// Write a program to print numbers from 1 to 10 using a for loop.

class test
{
    void display()
    {
        for(int i=1;i<=10;i++)
        {
            System.out.println(i);
        }
    }
}


class T9_L_Q01 {
    public static void main(String[] h) 
    {
        test t = new test();
        t.display();
    }
}